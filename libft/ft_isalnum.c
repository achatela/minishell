#include "libft.h"

int	ft_isalnum(int c)
{
	int	a;

	a = 0;
	if ((c <= 90 && c >= 65) || (c <= 122 && c >= 97)
		|| (c <= 57 && c >= 48))
		a = 1;
	else
		a = 0;
	return (a);
}
